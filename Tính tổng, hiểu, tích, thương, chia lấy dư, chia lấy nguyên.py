a = float(input("Nhập giá trị a: "))
b = float(input("Nhập giá trị b: "))
print("Tổng của 2 số là: ", a+b)
print("Hiệu của 2 số là: ", a-b)
print("Tích của 2 số là: ", a*b)
print("Thương của 2 số là: ", round(a/b, 2))
print("Thương của 2 số là: ", round(a/b, 3))
print("Chia lấy dư là: ", round(a%b, 2))
print("Chia lấy dư là: ", round(a%b, 3))
print("Chia lấy nguyên là: ", round(a//b, 2))
print("Chia lấy nguyên là: ", round(a//b, 3))



